<h3>Виробництво труб</h3>
<p>
	Виробництво сталевих труб може здійснюватись кількома способами.
</p>
Залежно від виду сировини і методу виготовлення розрізняють:
<ul>
	<li>Електрозварні прямошовні труби;</li>
	<li>Електрозварні спіралешовні;</li>
	<li>Безшовні гарячодеформовані;</li>
	<li>Безшовні холоднокатані.</li>
</ul><br>
<p>
	Основні етапи виробництва:
</p>
Електрозварні прямошовні
<ul>
	<li>Штрипс - згорнутий в рулон сталевий лист, розмотується і ріжеться на поздовжні смуги;</li>
	<li>Смуги зварюються в нескінченну стрічку, що необхідно для забезпечення безперервності виробництва;</li>
	<li>Стрічка в вальцах деформується, перетворюючись в круглу в перетині заготовку з відкритим швом;</li>
	<li>Шов проварюється. Застосовується кілька типів зварювання: дугова, індукційними струмами, лазером, плазмою і пучками електронів.</li>
</ul><br>
<p>
	Найбільш широко застосовуються два методи зварювання шва - TIG (електродугове зварювання вольфрамовим електродом в середовищі інертного газу) і HF (зварювання індукційними струмами високої частоти).
</p>
<p>
	Шов, виконаний першим методом зварювання, міцніший; проте другим - приблизно в 20 разів швидший.
	Відповідно зварені HF труби помітно дешевші.
</p>
<ul>
	<li>Потім сталева кругла труба калібрується в вальцях;</li>
	<li>Проходить неруйнівний контроль шва вихровими струмами або ультразвуком;</li>
	<li>Ріжеться на відрізки необхідної довжини і складaється.</li>
</ul><br>
<p>
	Характеристики готової продукції відповідають сертифікатам якості.
	Використовуються марки сталі Ст 1-3 пс / сп і Ст 08 пс / кп, діаметром від 10 до 1420 мм.
	Ознайомитися з сертифікатом прямошовних труб можна <a href="/upload/manufacture_electro-welded%20tube/truby-stalnye-elekstrosvarochnye.pdf" target="_blank">тут</a>.
</p>
<h3>Електрозварні спіралешовні</h3>
<p>
	Саме виробництво цих труб виглядає приблизно так само, як у прямошовних, проте стрічка заготовки згортається не в трубку, а в спіраль.
	Спіральний шов забезпечує трубі велику міцність на розрив.
</p>
<h3>Безшовні гарячодеформовані</h3>
Безшовна (або, як її ще називають, цільнотягнута) труба методом гарячої деформації виготовляється з циліндричної монолітної заготовки:
<ul>
	<li>Заготівля розігрівається в печі;</li>
	<li>Прошивний прес перетворює її в гільзу - порожній циліндр</li>
	<li>Гаряча гільза проходить кілька вальців, і витягаючи (звідси термін «цільнотянуті труби») формує потрібні діаметр і товщину стінок</li>
	<li>Потім готова кругла сталева труба остуджується, ріжеться по довжині і відправляється на склад готової продукції.</li>
</ul><br>
<h3>Безшовні холоднодеформовані</h3>
Відмінностей від попередніх труб всього дві:
<ul>
	<li>Після прошивного стану гільза охолоджується і всі наступні операції проходять в холодному вигляді;</li>
	<li>Після закінчення формування труби вона обов'язково випалюється - нагрівається до температури рекристалізації сталі і охолоджується.</li>
</ul><br>
<p>
	Це робить сталь більш в'язкою; крім того, при холодній деформації метал неминуче залишає внутрішня напруга.
</p>
<p>
	Ринок пропонує безшовні холоднодеформовані (холоднокатані) труби з товщиною стінок від 0,3 до 24 мм при діаметрі 5 - 250 мм.
</p>
<p>
	Труби з профільним перетином також виготовляються методами гарячої і холодної деформації за схожими алгоритмами, які використовуються у виробництві круглих труб.
	Кругла заготовка деформується на стані і набуває в результаті потрібну форму профілю.
</p>
<p>
	Труби профільні виготовляються з марок сталі Ст 2 пс / сп і Ст 08 кп, розмірами від 20х10 до 80х40 мм - прямокутні і від 10х10 до 60х60 - квадратні. Ви можете ознайомитися з сертифікатами, які встановлюють стандарти для <a target="_blank" href="/upload/manufacture_electro-welded%20tube/truby-profilnye-kvadratnye.pdf">квадратних</a> і <a target="_blank" href="/upload/manufacture_electro-welded%20tube/truby-profilnye-pryamokutnye.pdf">прямокутних</a> труб на нашому сайті.
</p>
