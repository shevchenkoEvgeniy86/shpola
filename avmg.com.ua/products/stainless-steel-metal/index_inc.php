<h3>Напрямок "Нержавіючий метал"</h3>
<p>
	АВ метал груп - компанія № 1 на українському ринку нержавіючої сталі, найбільший постачальник нержавіючого плоского прокату для української промисловості і один з лідерів в поставках зварних нержавіючих труб і дроту.
</p>

