<h3 class="text-uppercase">профнастил стеновой</h3>
<p>
	 Используется для обшивки фасадов зданий, стен, потолков, отделки или монтажа стен, ограждений, забора, ворот. Забор из профнастила можно сделать своими руками, крепится он зачастую на саморезы по металлу либо заклепки. Оцинкованный полимерный забор устойчив к воздействиям температуры и давления.
</p>