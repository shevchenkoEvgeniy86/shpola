<h3 class="text-uppercase">профнастил кровельный</h3>
<p>
	 Применяется как кровельный материал в частном и промышленном строительстве. Высота волны кровельного профлиста влияет на его несущую способность. Крыша из профнастила достаточно устойчива к внешним воздействиям. Ограждения, забор и ворота из профнастила за счет высокой волны имеют повышенную прочность.
</p>