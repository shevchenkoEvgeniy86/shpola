<h3>Направление "Электроды"</h3>
<p>
	Сварочные электроды - универсальный сварочный материал, предназначенный для ручной дуговой сварки, наплавки и резки углеродистых сталей, меди, алюминия, чугуна как в промышленности, так и в бытовых условиях.
</p>
<p>
	В процессе сварки, возникает потребность в высокой производительности, экономичности и экологичности.
	Поэтому производители электродов совершенствуют продукцию, выпуская новые марки электродов и модернизируя известные марки.
</p>
