<h3>Направление "Оцинкованный металл"</h3>
<p>
	С февраля 2014 открылось новое подразделение «АВ Металл Групп» - «Оцинкованный металлопрокат».
	Оцинкованный металл - покрытие металла цинком обеспечивает высокую коррозионную стойкость стали, что позволяет эксплуатировать готовые изделия в условиях агрессивных атмосферных воздействий на протяжении многих лет.
	Наиболее широко оцинковка используется в строительной сфере: листовой металл - лист оцинкованной стали, трубы из оцинкованной стали.
	Оцинковка в рулонах является основным материалом для производства дымоотводов, водостоков и систем вентиляции.
	Сталь оцинкованная гост широко используется в машиностроительном производстве, изготовлении бытовой и хозяйственных техники.
	Оцинкованная сталь цена на которую является конкурентоспособной, часто используется при изготовлении электротоваров.
</p>

