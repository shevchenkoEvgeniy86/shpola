<h3>Профнастил</h3>
<p>
	Профнастил - листовой профилированный материал из оцинкованной стали с нанесенным полимерным покрытием, имеющий несколько разновидностей: стеновой, кровельный и несущий.
	Профнастил часто используется в строительстве, в качестве кровельного материала, при производстве ворот, заборов и ограждений.
	Профильный лист считается наиболее надежным и экономичным материалом.
</p>
