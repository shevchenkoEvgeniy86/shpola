<h3>Направление "Сетка"</h3>
<p>
	Компания АВ металл групп является крупнейшим ритейлером металлической сетки и производителем кладочной сетки.
	Производство сварной сетки осуществляется путем сваривания прочной металлической проволоки.
	Как правило, в процессе производства используется точечная сварка.
	Размер ячеек сварной сетки бывает различным, сетка может быть оцинкованная и неоцинкованная.
	Область применения сварной сетки может быть самой разной: строительство (штукатурная сетка, сетка кладочная, сетка армирующая), промышленность, сельское хозяйство (защита от вредителей, клетки для животных и птиц).
	Сетка канилированная применяется для изготовления ограждений, клеток и вольеров для птиц и животных, изготовления ограждений для животных.
</p>
<p>
	Сетка кладочная обширно применяется в строительстве, для армирования бетонных конструкций.
	Просечно-вытяжная сетка изготавливается из металлического листа, ячейки.
	Ячейки просечно-вытяжной сетки расположены в шахматном порядке и имеют ромбическую форму.
	Сетка рабица используется во всех сферах строительства и промышленности.
	Сварная сетка предназначается для армирования практически всех элементов строения.
</p>
