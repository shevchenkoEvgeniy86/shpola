<h3>Направление "Черный металл"</h3>
<p>
	Основной деятельностью компании АВ металл групп является оптовая и розничная продажа черного металлопроката.
	Постоянно в наличии черный металл: арматура, катанка, круг, квадрат, уголок, швеллер, балка, г/к лист, х/к лист, оцинкованный лист, рифленый лист, ПВЛ, трубы, электроды.
	Купить черный металлопрокат оптом можно на наших складах по всей Украине: Киев, Днепр, Одесса, Харьков, Львов.
</p>

