 <br>
<p>
	 Этапы производства различных категорий труб также имеют существенные различия.
</p>
<h3>Производство электросварных прямошовных труб</h3>
 Электросварные прямошовные
<ul>
	<li>Листовая сталь, свернутая в рулон, подвергается продольной резке на полосы.</li>
	<li>Созданные заготовки (полосы) объединяются в одну непрерывную с помощью метода сваривания.</li>
	<li>Образовавшаяся лента подлежит последующей деформации в заготовку с круглым сечением, при этом остается открытым продольный шов.</li>
	<li>Соединительный шов сваривается несколькими типами сварки: индукционными токами, дуговой, плазмой, лазером и пучками электронов.</li>
</ul>
 <br>
<p>
	 Швы в электросварных трубах чаще всего формируются с помощью сварки индукционными токами (HF) и дуговой сварки вольфрамовым электродом в среде инертного газа.
</p>
<p>
	 Второй тип шва считается более прочным, однако первый метод сваривания осуществляется в 20 раз быстрее.
</p>
<ul>
	<li>После образования сварного шва труба подлежит калибровки в вальцах. </li>
	<li>Затем шов проходит контроль на точность, для этого используется ультразвук и вихревые токи.</li>
	<li>Откалиброванная труба формируется в отдельные отрезки. </li>
</ul>
 <br>
<p>
	 Готовая продукция соответствует сертификатам качества. Используются марки стали Ст 1-3 пс/сп и Ст 08 пс/кп, диаметром от 10 до 1420 мм. Ознакомиться с сертификатом электросварных прямошовных труб можно <a href="/upload/manufacture_electro-welded%20tube/truby-stalnye-elekstrosvarochnye.pdf" target="_blank">здесь</a>.
</p>
<h3>Производство электросварных спиральношовных труб</h3>
<p>
	 Данная труба имеет еще одно название — цельнотянутая, которое точно отражает метод ее производства. Бесшовная труба производится методом деформации цилиндрической заготовки под воздействием высоких температур.
</p>
<h3>Производство бесшовных горячедеформированных труб</h3>
 Бесшовная (или, как ее еще называют, цельнотянутая) труба методом горячей деформации производится из цилиндрической монолитной заготовки:
<ul>
	<li>Заготовка подлежит предварительному разогреву в специальной печи.</li>
	<li>Далее, производится первичная деформация прошивным прессом, в результате чего заготовка превращается в полый цилиндр (гильзу)</li>
	<li>Разогретая гильза вытягивается, проходя процесс вальцевания, при этом заготовка достигает установленных размеров: диаметра и толщины стенок.</li>
	<li>После этого производится поперечная нарезка трубы на нужные отрезки.</li>
</ul>
 <br>
<h3>Производство бесшовных холоднодеформированных труб</h3>
 Процедура производства данных труб схожа с предыдущим видом. Однако есть несколько отличий.
<ul>
	<li>После обработки под прошивным прессом, деформированная заготовка охлаждается и последующие процессы осуществляются в условиях низких температур. </li>
	<li>Уже сформированная труба проходит дополнительную процедуру отжигания, она нагревается до температуры рекристализации и охлаждается.</li>
</ul>
 <br>
<p>
	 Это делает сталь более вязкой; кроме того, металл покидают неизбежные при холодной деформации внутренние напряжения.
</p>
<p>
	 Профильных трубы изготавливаются методами горячей и холодной деформации по схожим алгоритмам, которые используются в производстве труб с круглым сечением. Круглая заготовка подвергается деформации на стане и обретает в результате нужную форму профиля.
</p>
<p>
	 Трубы профильные изготавливаются из марок стали Ст 2 пс/сп и Ст 08 кп, размерами от 20х10 до 80х40 мм - прямоугольные и от 10х10 до 60х60 - квадратные. Вы можете ознакомиться с сертификатами, которые устанавливают стандарты для <a href="/upload/manufacture_electro-welded%20tube/truby-profilnye-kvadratnye.pdf" target="_blank">квадратных</a> и <a href="/upload/manufacture_electro-welded%20tube/truby-profilnye-pryamokutnye.pdf" target="_blank">прямоугольных</a> труб на нашем сайте.
</p>
