<p>
	 Производство труб - широкий и многоэтапный процесс, в котором задействованы различные методы обработки металла.
</p>
 От выбора стали и метода производства зависит дальнейшая классификация труб. Они бывают:
<ul>
	<li>Бесшовные горячедеформированные </li>
